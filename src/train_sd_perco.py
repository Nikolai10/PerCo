def log_validation(vae, hyper_enc, processor, blip2, text_encoder, tokenizer, unet, args, accelerator, weight_dtype,
                   step, is_final_validation=False):
    logger.info("Running validation... ")

    pipeline = StableDiffusionPipelinePerco.from_pretrained(
        args.pretrained_model_name_or_path,
        vae=accelerator.unwrap_model(vae),
        hyper_enc=accelerator.unwrap_model(hyper_enc),
        text_encoder=accelerator.unwrap_model(text_encoder),
        tokenizer=tokenizer,
        unet=accelerator.unwrap_model(unet),
        safety_checker=None,
        revision=args.revision,
        variant=args.variant,
        torch_dtype=weight_dtype,
    )
    # update_scheduler(pipeline)
    pipeline = pipeline.to(accelerator.device, weight_dtype)
    pipeline.set_progress_bar_config(disable=True)

    if args.enable_xformers_memory_efficient_attention:
        pipeline.enable_xformers_memory_efficient_attention()

    if args.seed is None:
        generator = None
    else:
        generator = torch.Generator(device=accelerator.device).manual_seed(args.seed)

    validation_images = args.validation_image

    image_logs = []

    inference_ctx = contextlib.nullcontext() if is_final_validation else torch.autocast("cuda")

    for validation_image in validation_images:
        validation_image = Image.open(validation_image).convert("RGB")
        random_crop_512 = RandomCrop((512, 512))
        validation_image = random_crop_512(validation_image)

        inputs = processor(images=validation_image, return_tensors="pt").to(accelerator.device, weight_dtype)
        generated_ids = blip2.generate(**inputs, max_length=cfg_perco.max_number_tokens)
        validation_prompt = processor.batch_decode(generated_ids, skip_special_tokens=True)[0].strip()
        validation_image_tensor = ToTensor()(validation_image)
        validation_image_tensor = validation_image_tensor * 2. - 1.

        images = []

        latents = pipeline.vae.encode(
            (validation_image_tensor).unsqueeze(0).to(accelerator.device, weight_dtype)).latent_dist.sample()
        latents = latents * pipeline.vae.config.scaling_factor

        pipeline.hyper_enc.quantizer.eval()
        hyper_latent = pipeline.hyper_enc(latents)
        z_hat = hyper_latent.z_hat

        for _ in range(args.num_validation_images):
            with inference_ctx:
                image = pipeline(
                    validation_prompt,
                    z_hat,
                    height=512,
                    width=512,
                    num_inference_steps=cfg_perco.num_inference_steps,
                    guidance_scale=cfg_perco.guidance_scale,
                    generator=generator
                ).images[0]
            images.append(image)

        image_logs.append(
            {"validation_image": validation_image, "images": images, "validation_prompt": validation_prompt}
        )

    tracker_key = "test" if is_final_validation else "validation"
    for tracker in accelerator.trackers:
        if tracker.name == "tensorboard":
            for log in image_logs:
                images = log["images"]
                validation_prompt = log["validation_prompt"]
                validation_image = log["validation_image"]

                formatted_images = []

                formatted_images.append(np.asarray(validation_image))

                for image in images:
                    formatted_images.append(np.asarray(image))

                formatted_images = np.stack(formatted_images)

                tracker.writer.add_images(validation_prompt, formatted_images, step, dataformats="NHWC")
        elif tracker.name == "wandb":
            formatted_images = []

            for log in image_logs:
                images = log["images"]
                validation_prompt = log["validation_prompt"]
                validation_image = log["validation_image"]

                formatted_images.append(wandb.Image(validation_image, caption="Controlnet conditioning"))

                for image in images:
                    image = wandb.Image(image, caption=validation_prompt)
                    formatted_images.append(image)

            tracker.log({tracker_key: formatted_images})
        else:
            logger.warning(f"image logging not implemented for {tracker.name}")

    pipeline.hyper_enc.quantizer.train()

    del pipeline
    gc.collect()
    torch.cuda.empty_cache()

    return image_logs


